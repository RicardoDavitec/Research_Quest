import { NestFactory } from '@nestjs/core';
import { ValidationPipe } from '@nestjs/common';
import { SwaggerModule, DocumentBuilder } from '@nestjs/swagger';
import { AppModule } from './app.module';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  // Global validation pipe
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  );

  // CORS configuration
  app.enableCors({
    origin: process.env.CORS_ORIGIN || 'http://localhost:3000',
    credentials: true,
  });

  // Swagger documentation
  const config = new DocumentBuilder()
    .setTitle('Campo Research Platform API')
    .setDescription('API para Plataforma de Desenvolvimento de Question√°rios de Pesquisas de Campo')
    .setVersion('0.1.0')
    .addBearerAuth()
    .addTag('auth', 'Autentica√ß√£o e autoriza√ß√£o')
    .addTag('subgroups', 'Gest√£o de subgrupos de pesquisa')
    .addTag('researchers', 'Gest√£o de pesquisadores')
    .addTag('questions', 'Gest√£o de quest√µes')
    .addTag('questionnaires', 'Cria√ß√£o de question√°rios')
    .addTag('surveys', 'Pesquisas operacionais')
    .addTag('similarity', 'Detec√ß√£o de similaridade')
    .build();
  
  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api/docs', app, document);

  const port = process.env.PORT || 3001;
  await app.listen(port);
  
  console.log(`üöÄ Application is running on: http://localhost:${port}`);
  console.log(`üìö API Documentation: http://localhost:${port}/api/docs`);
}

bootstrap();
